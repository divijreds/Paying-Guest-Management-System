    public static void _main() {
        Scanner s = new Scanner(System.in);

        int no_of_houses = 0;
        House[] houses = new House[5];
        int choice = 0;
        
        System.out.println("Welcome Owner!");

        do{
            System.out.println("Select any one option:");
            System.out.println("    [1] List all Houses");
            System.out.println("    [2] Add House");
            System.out.println("    [3] Manage House");
            System.out.println("    [4] Remove House");
            System.out.println("    [9] Exit");

            choice = s.nextInt();

            switch(choice) {
            case 1:
                for(int i = 0; i < no_of_houses; i++) {
                    System.out.println( "   #0: " + houses[i] );
                }               
                break;

            case 2:
                System.out.println("Enter name of the new house >>");
                String name = s.next();
                System.out.println("Enter address of the new house >>");
                String address = s.next();
                System.out.println("Enter total accomodation capacity >>");
                int capacity = s.nextInt();
                System.out.println("Enter rent per head >>");
                int rent = s.nextInt();
                
                houses[no_of_houses] = new House(name, address, capacity, rent);
                no_of_houses++;
                break;

            case 3:
                System.out.println("Enter the House index to manage >> ");
                int number_to_manage = s.nextInt();
                houses[number_to_manage].manage_house();
                break;

            case 4:
                System.out.println("Enter the House number to delete >> ");
                int number_to_del = s.nextInt();
                houses[number_to_del] = null;
                no_of_houses--;
                break;

            default:
                System.out.println("Invalid Input");
                break;
            }
        }       
        while(choice != 9);
    }
    
