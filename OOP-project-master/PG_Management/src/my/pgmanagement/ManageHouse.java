    private void manageHouse(String name) {
        try {
        
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pgmanagement","root","");
            PreparedStatement p = con.prepareStatement("select * from houses where name = ?");
            p.setString(1,name);
            
            ResultSet rs = p.executeQuery();

            while( rs.next() ) {
                String name3 = rs.getString(1);
                jTextArea1.append(name3 + "\n");
                //jLabel3.setText(name3 + "\n");
            }
            
            con.close();
            p.close();

        }
        catch(Exception ex) {
            ex.printStackTrace();
        }
    }
    
