    private void addHouse(String name, String address, int capacity, int rent) {
        try {
        
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pgmanagement","root","");
            PreparedStatement p = con.prepareStatement("insert into houses values(?,?,?,?)");
            p.setString(1,name);
            p.setString(2,address);	
            p.setInt(3,capacity);
            p.setInt(4,rent);

            int result = p.executeUpdate();
            if(result == 1) {
                System.out.println("Add query executed");
            }
            else {
                System.out.println("ERROR in adding new house");
            }
            
            con.close();
            p.close();

        }
        catch(Exception ex) {
            //ex.printStackTrace();
        }
    }
    
